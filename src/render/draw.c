#include "minirt.h"

void draw_image(t_data *data)
{
	int x;
	int y;

	x = 0;
	while (x < WIDTH)
	{
		y = 0;
		while (y < HEIGHT)
		{
			img_put_pixel(data, x, y, 0xFFFF0000);
			y++;
		}
		x++;
	}
}
